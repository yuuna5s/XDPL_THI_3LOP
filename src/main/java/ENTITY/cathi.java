package ENTITY;

public class cathi {
    String idcathi;
    String giothi;

    public cathi(String idcathi, String giothi) {
        this.idcathi = idcathi;
        this.giothi = giothi;
    }

    public cathi(){}

    public String getIdcathi() {
        return idcathi;
    }

    public void setIdcathi(String idcathi) {
        this.idcathi = idcathi;
    }

    public String getGiothi() {
        return giothi;
    }

    public void setGiothi(String giothi) {
        this.giothi = giothi;
    }
}
